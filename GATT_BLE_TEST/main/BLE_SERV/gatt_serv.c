        break;
    case ESP_GATTS_CONF_EVT:
        ESP_LOGI(tag_b, "ESP_GATTS_CONF_EVT status %d attr_handle %d", param->conf.status, param->conf.handle);
        if (param->conf.status != ESP_GATT_OK){
            esp_log_buffer_hex(tag_b, param->conf.value, param->conf.len);
        }
